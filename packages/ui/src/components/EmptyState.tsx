export type EmptyStateIcon = keyof typeof icons

export type EmptyStateProps = {
  /** Main heading */
  title: string
  /** Supporting description */
  description?: string
  /** Optional action button or link */
  action?: React.ReactNode
  /** Icon key or custom React node */
  icon?: EmptyStateIcon | React.ReactNode
  /** Additional classes */
  className?: string
  /** Compact mode for inline/card usage */
  compact?: boolean
}

/**
 * EmptyState - A consistent component for displaying when lists/grids have no data
 * 
 * Usage:
 * ```tsx
 * <EmptyState 
 *   icon="inbox"
 *   title="No messages" 
 *   description="You don't have any messages yet."
 *   action={<Button>Compose</Button>}
 * />
 * ```
 */
export const EmptyState: React.FC<EmptyStateProps> = ({
  title,
  description,
  action,
  icon = 'inbox',
  className,
  compact = false,
}) => {
  // Resolve icon - either a key or custom node
  const iconNode = typeof icon === 'string' && icon in icons 
    ? icons[icon as EmptyStateIcon] 
    : icon

  return (
    <div
      className={`
        flex flex-col items-center justify-center text-center
        ${compact ? 'py-6 px-4' : 'py-12 px-6'}
        rounded-2xl border border-[var(--color-border,#e5e7eb)] 
        bg-gradient-to-b from-[var(--color-card,#ffffff)] to-[var(--color-card-alt,#fafafa)]
        ${className || ''}
      `.trim().replace(/\s+/g, ' ')}
    >
      {/* Icon container with subtle background */}
      <div className={`
        ${compact ? 'mb-3' : 'mb-4'}
        flex items-center justify-center
        ${compact ? 'w-14 h-14' : 'w-20 h-20'}
        rounded-2xl
        bg-gradient-to-br from-gray-100 to-gray-50
        text-gray-400
      `}>
        {iconNode}
      </div>
      
      {/* Title */}
      <h3 className={`
        font-semibold text-[var(--color-foreground,#1f2937)]
        ${compact ? 'text-base' : 'text-lg'}
      `}>
        {title}
      </h3>
      
      {/* Description */}
      {description && (
        <p className={`
          text-[var(--color-muted,#6b7280)] max-w-sm
          ${compact ? 'text-sm mt-1' : 'text-base mt-2'}
        `}>
          {description}
        </p>
      )}
      
      {/* Action */}
      {action && (
        <div className={compact ? 'mt-3' : 'mt-4'}>
          {action}
        </div>
      )}
    </div>
  )
}

export default EmptyState
